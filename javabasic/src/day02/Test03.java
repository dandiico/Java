package day02;

public class Test03 {

	public static void main(String[] args) {
		
		int i = 100;
		System.out.println("i : " + i);
		
		long l = 10L; // 가독성을 위해 접미사를 붙여주기
		System.out.println("l : " + l);
		
		double d = 123.456;
		System.out.println("d : " + d);
		
		float f = 123.456F;
		System.out.println("f : " + f);
		
		
	}

}